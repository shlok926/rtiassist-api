// Current UI language
let currentUILanguage = localStorage.getItem('ui_language') || 'english';

// Function to get translated text
function t(key) {
  return UI_TRANSLATIONS[currentUILanguage][key] || UI_TRANSLATIONS.english[key] || key;
}

// Function to update all UI text
function updateUILanguage(lang) {
  currentUILanguage = lang;
  localStorage.setItem('ui_language', lang);
  
  // Update all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    const translation = t(key);
    
    // Handle different element types
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.hasAttribute('placeholder')) {
        el.placeholder = translation;
      }
    } else if (el.tagName === 'OPTION') {
      el.textContent = translation;
    } else {
      // For regular elements, preserve HTML structure
      // Only update text nodes, not nested elements
      if (el.children.length === 0) {
        el.textContent = translation;
      } else {
        // Handle elements with child nodes
        updateElementWithChildren(el, translation);
      }
    }
  });
  
  // Update language toggle button
  const langToggle = document.getElementById('langToggleBtn');
  if (langToggle) {
    langToggle.textContent = lang === 'english' ? '🌐 हिन्दी' : '🌐 English';
  }
  
  // Update page title
  document.title = lang === 'english' 
    ? 'RTIAssist — India\'s Most Powerful Free RTI Tool'
    : 'RTIAssist — भारत का सबसे शक्तिशाली मुफ्त RTI टूल';
}

// Helper function to update elements with children
function updateElementWithChildren(el, translation) {
  // Find the first text node and update it
  for (let node of el.childNodes) {
    if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
      node.textContent = translation;
      break;
    }
  }
}

// Toggle language function
function toggleUILanguage() {
  const newLang = currentUILanguage === 'english' ? 'hindi' : 'english';
  updateUILanguage(newLang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  updateUILanguage(currentUILanguage);
});

// Export for use in other scripts
window.t = t;
window.updateUILanguage = updateUILanguage;
window.toggleUILanguage = toggleUILanguage;
window.currentUILanguage = () => currentUILanguage;
